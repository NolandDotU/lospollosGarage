    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        // TODO add your handling code here:
        if (validateFields()) {
        int selectedRow = tabelCustomer.getSelectedRow();
        if (selectedRow != -1) {
            tableModel.setValueAt(txtName.getText(), selectedRow, 1);
            tableModel.setValueAt(txtNoTelp.getText(), selectedRow, 2);
            tableModel.setValueAt(txtEmail.getText(), selectedRow, 3);
            javax.swing.JOptionPane.showMessageDialog(this, "Customer Updated Successfully");
            resetFields();
            btnUpdate.setEnabled(false); // Disable update after successful update
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "Please select a row to update.");
        }
    } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields before updating.");
    }
    }//GEN-LAST:event_btnUpdateActionPerformed
                        
    
    private void btnResetActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnResetActionPerformed
        tableModel.setRowCount(0); // Clears all data from the table
    javax.swing.JOptionPane.showMessageDialog(this, "Table Data Cleared");
    resetFields();


    }//GEN-LAST:event_btnResetActionPerformed

    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if (validateFields()) {
        customerPK++;
        tableModel.addRow(new Object[] {
            customerPK,
            txtName.getText(),
            txtNoTelp.getText(),
            txtEmail.getText()
        });
        javax.swing.JOptionPane.showMessageDialog(this, "Customer Added Successfully");
        resetFields();
        } else {
        javax.swing.JOptionPane.showMessageDialog(this, "Please fill all fields before saving.");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

