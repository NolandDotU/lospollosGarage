    private void tabelCustomerMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tabelCustomerMouseClicked
        // TODO add your handling code here:
        int selectedRow = tabelCustomer.getSelectedRow();
    if (selectedRow != -1) {
        txtName.setText(tableModel.getValueAt(selectedRow, 1).toString());
        txtNoTelp.setText(tableModel.getValueAt(selectedRow, 2).toString());
        txtEmail.setText(tableModel.getValueAt(selectedRow, 3).toString());
        btnUpdate.setEnabled(true); // Enable update button
    }
    }//GEN-LAST:event_tabelCustomerMouseClicked
         
